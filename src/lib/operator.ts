export type TaskRealm = "digital" | "physical";
export type TaskStatus = "todo" | "doing" | "review" | "done" | "blocked";
export type TaskPriority = "p0" | "p1" | "p2" | "p3";
export type TaskOwnerKind = "agent" | "human";

export type TaskSource =
  | "matrix" // native Matrix task
  | "linear"
  | "notion"
  | "asana"
  | "trello"
  | "jira"
  | "github"
  | "oracle"; // generated by Oracle suggestion

export interface Task {
  id: string;
  workspaceId: string;
  title: string;
  description?: string;
  realm: TaskRealm;
  status: TaskStatus;
  priority: TaskPriority;
  ownerName: string;
  ownerKind: TaskOwnerKind;
  tags: string[];
  // Dates (ISO)
  createdAtIso: string;
  dueAtIso?: string;
  startedAtIso?: string;
  completedAtIso?: string;
  // Integration tracking
  source: TaskSource;
  externalId?: string;
  externalUrl?: string;
  lastSyncedAtIso?: string;
  // Cross-linking with other Matrix entities
  relatedRockId?: string;
  relatedIssueId?: string;
  relatedWorkflowId?: string;
  relatedAgentId?: string;
  // Effort estimation
  estimatedMinutes?: number;
  actualMinutes?: number;
  // Approval requirement (for external-send tasks)
  requiresApproval?: boolean;
}


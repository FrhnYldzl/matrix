// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

export function tasksForWorkspace(wsId: string): Task[] {
  return tasks.filter((t) => t.workspaceId === wsId);
}

export function tasksByStatus(tasks: Task[]): Record<TaskStatus, Task[]> {
  const out: Record<TaskStatus, Task[]> = {
    todo: [],
    doing: [],
    review: [],
    done: [],
    blocked: [],
  };
  for (const t of tasks) out[t.status].push(t);
  return out;
}

export function tasksByRealm(tasks: Task[]): Record<TaskRealm, Task[]> {
  return {
    digital: tasks.filter((t) => t.realm === "digital"),
    physical: tasks.filter((t) => t.realm === "physical"),
  };
}

/**
 * "Overdue" — dueAtIso geçmiş ve henüz done değil.
 */
export function isOverdue(t: Task, nowIso: string = new Date().toISOString()): boolean {
  if (!t.dueAtIso || t.status === "done") return false;
  return t.dueAtIso < nowIso;
}
